namespace root;

public class Pokemon
{
    private int hp;
    private int attack;
    private int defense;
    private int speed;
    private int accuracy;


    public Pokemon(int hp, int attack, int defense, int speed, int accuracy)
    {
        this.hp = hp;
        this.attack = attack;
        this.defense = defense;
        this.speed = speed;
        this.accuracy = accuracy;
    }
}